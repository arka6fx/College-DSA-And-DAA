#include <stdio.h>

int main() {
    printf("Ar\nka   \n");// go to new line
    printf("Ar\bka   \n");// backspace
    printf("ar\tka  \n");// 1tab = 4 spaces
    printf("\"arka\" \n"); //printf along with quotation
    printf("\\arka\\  \n");// to print backslash
    printf("\\n arka  \n");// print\n
    printf("\a");//beep sound
    return 0;
}
